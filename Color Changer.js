
function setup() {
  createCanvas(400,400);



  

 

}



function draw() {

  if (keyDown(DOWN_ARROW)) {
    background ('red')
  }


  if (keyDown(UP_ARROW)) {
    background ('purple')
  }


  if (keyDown(RIGHT_ARROW)) {
    background ('yellow')
  }


  if (keyDown(LEFT_ARROW)) {
    background ('LightSeaGreen')
  }
  

}



